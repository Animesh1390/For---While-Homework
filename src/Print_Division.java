

public class Print_Division
{
    public static void main(String[] args)  // main method
    {

        System.out.println(" Divide by 3: ");         // for user input

        for (int i = 1; i < 100; i++)                // for loop
        {
            if (i % 3 == 0)
                System.out.print(i + ",");
        }


        System.out.print("\n");                      //  just for better space in console


        System.out.println(" Divide by 5: ");
        for (int i = 1; i < 100; i++) {
            if (i % 5 == 0)
                System.out.print(i + ",");        // print out output



    }
        }


    }

